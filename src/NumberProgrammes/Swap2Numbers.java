package NumberProgrammes;

public class Swap2Numbers {
	
	public static void main(String[] args) {
		
		int x,y,t;
		x=10;
		y=20;
		
		System.out.println("before swapping");
		System.out.println(x);
		System.out.println(y);		
		//swapping logic
		t= x;
		x= y;
		y= t;		
		System.out.println("after swapping");
		System.out.println(x);
		System.out.println(y);
	
		
		
	}

}
